-- Roles
CREATE TABLE IF NOT EXISTS Roles (
    roleId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    roleName VARCHAR(255) NOT NULL UNIQUE
);

-- Users
CREATE TABLE IF NOT EXISTS Users(
    userId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(255) NOT NULL UNIQUE,
    email VARCHAR(255) NOT NULL UNIQUE,
    description VARCHAR(255),
    password VARCHAR(255) NOT NULL
           );

-- UserRoles
CREATE TABLE IF NOT EXISTS UserRoles(
    user_id INT REFERENCES Users(userId) ON DELETE CASCADE,
    role_id INT REFERENCES Roles(roleId) ON DELETE CASCADE,
    PRIMARY KEY (user_id, role_id)

);